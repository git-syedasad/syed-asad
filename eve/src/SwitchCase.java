public class SwitchCase {
public static void main(String args[])
{
String month="march";
switch(month) {
	case "january":{
		System.out.println("its my birthday.....");
		break;
	}
	case "february":{
		System.out.println("its not my birthday.....");
		break;
	}
	default:{
		System.out.println("its my friends birthday");
	}
	}
}
}
